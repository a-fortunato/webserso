					<h4>Día 01 : El Pireo Atenas - 11:00</h4>
					<p>Cuna de la civilización griega y de una cultura que se
						presenta como siendo una de las más potentes y importantes de la
						historia, Atenas, hoy en dia es una verdadera metrópoli. La
						tradición mitológica referente a su nacimiento es fascinante:
						Atenas era el asunto de una riña entre los Díos que encontraron
						una solución gracias a una oferta de paz de Atena, Díosa de la
						sabiduría. Es por eso que el consejo de los Díos la proclamaron
						Díosa de la ciudad. El lugar central de la civilisación griega
						está evidentemente representado por el Acrópolis, sitio
						resguardado y funcional que sirve de refugio alrededor del palacio
						fortificado. El primer templo dedicado a Atena dió lugar a la
						transformación de la fortaleza en un sanctuario, por lo cual la
						belleza dominaba el culto mismo de la divinidad.</p>

					<h4>Día 01 : Mykonos 18:00 - 23:00</h4>
					<p>Situada entre Tinos y Naxos es, con Satorin, la isla más
						fascinante del archipiélago de los Ciclades. Pero al contrario de
						Santorin se saben pocas cosas de las origenes de Mykonos. Según
						los historiadores, la isla hubiera sido colonizada por los Iónos
						pero fueron los Atenienses que la explotaron más todavía. Desde
						hace 50 años, los habitantes de la isla han descubierto los
						recursos ofrecidos por la industría turistica que, en nuestros
						días, representa el mayor recurso de Mykonos. Se dice que hay un
						sitio donde cada señorita bebiendo en una de las tres fuentes de
						la plaza es cierta, según la tradición, de encontrar un marido
						antes del fin del año.</p>

					<h4>Día 2 : Kusadasi 07:00 - 12:00</h4>
					<p>El principal encanto de este puerto turco es de permitir el
						acceso a Efeso, uno de los mayores sitios arqueológicos del mundo.
						Su famoso templo de Artemis es una de las siete maravillas del
						mundo. No hay que perderse una excursión a Efeso : allí, la
						historia cobra vida. Al caminar hacia el puerto antiguo lo largo
						de una carretera de mármol usada por las ruedas de incontables
						carretas, bordea las maravillas arquitecturales como la Puerta de
						Magnesia y la Biblioteca de Celsius, antes de llegar al Gran
						Anfiteatro donde San Pablo fue arrestado antes de ser expulsado de
						la Ciudad.</p>

					<h4>Día 2 : Patmos 16:00 - 21:00</h4>
					<p>Una de las mas pequeñas islas del Dodecaneso, Patmos es una
						isla cuya historía politica y religiosa es importante. Durante los
						días de los antiguos griegos, Artemis, hermana gemela de Apolón y
						la díosa de la caza fueron venerados por los autóctonos. Los
						antiguos ocupantes de la isla fueron los cruzados, los turcos y
						los italianos. Quizás el periodo más significado de la historía
						fue la del primer siglo antes de Jesus Cristo cuando San John el
						evangelista ha sido desterrado por el emperador Domitian, en
						Patmos, para predicar el cristianismo a Ephesus. Ha establecido
						una colonía cristiana en Patmos y ha escrito el Libro de las
						revelaciones en la cueva de Chora. El monasterio de San John ha
						sido erigido durante el siglo 11 y es un homenaje a las
						predicaciones del San Patrono.</p>

					<h4>Día 3 : Rodas 07:00 - 18:00</h4>
					<p>Una larga serie de leyendas y de mitos rellenan la historia
						de Rodas, una de las principales islas del Mediterráneo, la cuarta
						del archipiélago griego y la más importante del Dodecaneso. Según
						la mitología griega, la isla seria un regalo dejado a los hombres
						por el Dios Helios que se enamoró de Roda y le dedicó la isla y
						sus bellezas. En realidad , existe una explicación de la
						importancia de esta isla: durante el periodo griego conoció la
						fama que se podía comparar en aquel tiempo a la de Atenas. La
						ciudad todavía está dividida en dos partes muy distintas: la parte
						norte es la parte moderna y actual con nuevas estructuras y la
						parte sudeste saca sus origenes del periodo medieval.</p>

					<h4>Día 4 : Heraklion 07:00 - 11:30</h4>
					<p>La fundación de Heraklion es bastante reciente. En la
						Antigüedad, el pueblo fue utilizado como escala maritima de
						Cnossos, famosa en las leyendas griegas por su palacio del
						Minotor. Pero el verdadero nacimiento de la ciudad es debido a los
						alforfónes que le daron en el siglo 9 su fisionomía actual. No hay
						dudas, lo que da a la Creta su mayor atracción no son unicamente
						los monumentos historicos de Heraklion sino también que es una de
						las ciudades más resplandecientes de la civilisación griega. Nos
						permite volver en los tiempos de la dinastía de los Minos.
						Numerosos pueblos, sobretodo en el interior, han conservado la
						mentalidad, las costumbres y las tradiciones antiguas.</p>

					<h4>Día 4 : Santorini 16:30 - 21:00</h4>
					<p>Callisti: su nombre griego significaba "la mas bella".
						Santoríni es una de las islas del archipiélago de los Ciclades más
						hechizadora y una de las más visitadas por los turistas. Santoríni
						ofrece una variedad impresionante de paisajes: el litoral
						occidental está formado por unas rocas vertiginosas, mientrás que
						la costa oriental se extiende con más suavidad. A cada curva se
						presenta el mar, las otras islas del archipiélago y los otros
						promontorios en el mar. Pero no soló se trata de la naturaleza
						sino también de las excavaciones archeológicas recientes que han
						confirmado la ocupación de la isla por los fenicianos y sin
						ninguna duda durante los tiempos prehistoricos.</p>

