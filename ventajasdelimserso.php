	<h1>¡Qué ganas de jubilarme!</h1>
	<div class="novedades">

		<p>A pesar de que este deseo se anhela durante mucho tiempo,
			cuando llega el momento de disfrutar de la jubilación los días
			empiezan a tener para muchas personas en esta situación 48 horas,
			y aprovechar este tiempo libre es una preocupación para los
			propios jubilados y para sus familias. Muchas instituciones, tanto
			públicas como privadas, son conscientes de esta realidad y por
			ello ponen a disposición de los mayores un gran número de
			actividades lúdicas, culturales y educativas. Disfrutar de viajes,
			estudios en la universidad, una variada oferta de cursos, acceso a
			museos, parques temáticos o teatros, todo ello a precios
			reducidos, son algunas de las ventajas de cumplir los 65.</p>
